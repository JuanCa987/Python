"""Los operadores matematicos en python permiten realizar operaciones aritmeticas basicas
como suma,resta,multiplicacion,etc.."""

friends = 10

#suma
friends += 1

#resta 
friends -= 2

#multiplicacion
friends *= 4

# division(Divide un número entre otro y siempre retorna un valor flotante.)
friends /= 2

#division entera(retorna solo la parte entera del cociente.)
friends //= 3

# modulo(Retorna el resto de la división entre dos números.)
friends %= 3

#exponente
friends **= 2


print(friends)